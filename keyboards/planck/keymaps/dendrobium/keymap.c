bool process_record_user (uint16_t keycode, keyrecord_t *record) {
	if (record->event.pressed) switch (keycode) {
		case LR_PRN: SEND_STRING("()"SS_TAP(X_LEFT)); return false;
		case LR_CBR: SEND_STRING("{}"SS_TAP(X_LEFT)); return false;
		case LR_BRC: SEND_STRING("[]"SS_TAP(X_LEFT)); return false;
		case LR_CMP: SEND_STRING("<>"SS_TAP(X_LEFT)); return false;
	} return true;
}
